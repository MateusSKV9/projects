// variáveis
let x = 5;
let y = 6;
let z = x + y;
console.log(x + " + " + y + " = " + z);

// números literais
console.log(10.5);
console.log(1001);

// strings literais
console.log("Mateus Santos");
console.log("JavaScript");

// let
let k = 9;
console.log(k);

// const
const fname = "Mateus";
console.log(fname);

// operador de atribuição (=)
let a = 1;
let b = 2;
let sum = a + b;
console.log(sum);

// operador aritméticos (+ - * /)
console.log((5 + 6) * 10);
console.log(b * 10);
console.log("Ana" + "Maria");

// case sensitive
let lastName = "Doe";
let lastname = "Peterson";
console.log(lastName);
console.log(lastname);

// Underscore
let first_name = "Mateus";
console.log(first_name);

// Pascal Case
let FirstName = "Mateus";
console.log(FirstName);

// Camel Case
let firstName = "Mateus";
console.log(firstName);

